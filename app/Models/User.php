    public static function rules($id = null)
    {
        return [
            'name' => 'required|string|max:255',
            'email' => [
                'required',
                'string',
                'email',
                'max:255',
                Rule::unique('users')->ignore($id),
            ],
            'password' => 'required|string|min:8',
        ];
    }
    public function answers()
    {
        return $this->hasMany(Answer::class);
    }

}
